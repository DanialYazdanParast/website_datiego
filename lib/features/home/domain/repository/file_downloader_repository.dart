/// رابط (Interface) برای مخازن مرتبط با دانلود فایل.
///
/// این کلاس یک رابط انتزاعی (Abstract) است که عملیات دانلود فایل را تعریف می‌کند.
/// هر پیاده‌سازی از این رابط باید متد `downloadFile` را پیاده‌سازی کند.
abstract class FileDownloaderRepository {
  /// دانلود فایل از آدرس مشخص شده و ذخیره آن با نام فایل داده شده.
  ///
  /// ## پارامترها:
  /// - [url]: آدرس URL فایل برای دانلود.
  /// - [fileName]: نام فایل برای ذخیره در دستگاه.
  ///
  /// ## استثناها:
  /// - ممکن است خطاهایی مانند عدم دسترسی به اینترنت، خطای سرور، یا مشکلات ذخیره‌سازی رخ دهد.
  Future<void> downloadFile(String url, String fileName);
}
